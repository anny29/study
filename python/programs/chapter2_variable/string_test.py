#首字母大写
name = "li zhao mu"
print(name.title())

#字符串可以用双引号或者单引号，在双引号字符串中可方便嵌入单引号
desc = "'zhongwen' print 'luan ma'"
print(desc)

# \n 换行符，\t制表符
line = "first line\n\tsecond line"
print(line)

# 去空格函数 strip() rstrip() lstrip()
language = " python "
print(language.lstrip())
print(language.rstrip())
print(language.strip())
