#3-8 排序
print("\n3-8 排序")
dest = ["japan", "america", "eruope", "austrilia", "home"]

print("original")
print(dest)

print("sorted")
print(sorted(dest))

print("sorted reverse")
print(sorted(dest, reverse=True))

print("after sorted")
print(dest)

print("reverse")
dest.reverse()
print(dest)

print("reverse again")
dest.reverse()
print(dest)

print("sort")
dest.sort()
print(dest)

print("sort reverse")
dest.sort(reverse=True)
print(dest)
